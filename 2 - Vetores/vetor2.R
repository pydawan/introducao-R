#!/usr/bin/env Rscript

# Vetores são arrays (ou arranjo, em Português) de uma dimensão
# que podem armazenar numerics, character ou logical.

# Em R, você pode criar um vetor utilização a função Combine c().
numeric_vector <- c(1, 10, 49)
character_vector <- c("a", "b", "c")

# Complete o código para a variável boolean_vector
boolean_vector <- c(TRUE, FALSE, TRUE)

numeric_vector
character_vector
boolean_vector
